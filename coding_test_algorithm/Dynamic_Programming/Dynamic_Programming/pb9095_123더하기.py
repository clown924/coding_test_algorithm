# 1 = (1) => 1개
# 2 = (1 + 1), (2) => 2개
# 3 = (1 + 1 + 1), (1 + 2), (2 + 1), (3) => 4개
# 4 = (1 + 1 + 2), (2 + 2), (3 + 1), (1 + 3), (1 + 2 + 1),
#     (2 + 1 + 1), (1 + 1 + 1 + 1) => 7

# 구한 점화식
# dp[i] = dp[i-1] + dp[i-2] + dp[i-3]

T = int(input())

for _ in range(T):
    n = int(input())
    dp = [0] * 1001
    dp[1] = 1
    dp[2] = 2
    dp[3] = 4
    for i in range(4,n+1):
        dp[i] = dp[i-1] + dp[i-2] + dp[i-3]
    print(dp[n])
